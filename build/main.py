def add_nums(num1: int, num2: int) -> int:
    """
    Function to add numbers.

    Parameters
    ----------
    num1 : int
        First number.
    num2 : int
        Second Nnumber.

    Returns
    -------
    int
        Sum of both the numbers.

    """
    return num1 + num2


